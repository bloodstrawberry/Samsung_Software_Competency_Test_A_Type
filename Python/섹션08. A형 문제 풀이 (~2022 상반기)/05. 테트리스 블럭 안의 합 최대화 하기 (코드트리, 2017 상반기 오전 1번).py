def input_data():
    global N, M, MAP
    
    N, M = map(int, input().split())
    
    MAP = [[0] * MAX for _ in range(MAX)]
    
    for r in range(N):
        MAP[r][:M] = map(int, input().split())

def print_map(): # for debug
    for r in range(N):
        print(*MAP[r][:M])
    print("") 

def scan(blockNum, sr, sc):
    sum = 0
    for r in range(4):
        for c in range(4):
            sum += (MAP[sr + r][sc + c] * BLOCK[blockNum][r][c])
            
    return sum

